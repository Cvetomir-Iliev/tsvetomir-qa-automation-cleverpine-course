// Function declaration syntax:
function functionName() {
  // Code to execute
  return "something"; // Optional
}

// Simple example: A function that greets users
function welcomeMessage() {
  console.log("Welcome to the QA Automation Testing Suite!");
}

// Call the function (execute it)
welcomeMessage(); // Output: "Welcome to the QA Automation Testing Suite!"

// You can call it multiple times
welcomeMessage();
welcomeMessage();
